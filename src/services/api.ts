// REALTIME SUBSCRIPTIONS
// =============================================

export function subscribeToEmployees(callback: () => void) {
  return supabase
    .channel('employees-changes')
    .on('postgres_changes', { event: '*', schema: 'public', table: 'employees' }, callback)
    .subscribe()
}

export function subscribeToRequests(callback: () => void) {
  return supabase
    .channel('requests-changes')
    .on('postgres_changes', { event: '*', schema: 'public', table: 'access_requests' }, callback)
    .subscribe()
}
