// DASHBOARD STATS
// =============================================

export async function getDashboardStats() {
  // Fetch in parallel
  const [
    { count: totalUsers },
    { count: activeUsers },
    { count: highRiskUsers },
    { count: pendingReviews },
    { count: pendingRequests },
    { count: joiners },
    { count: movers },
    { count: leavers },
    { count: unresolvedInsights },
  ] = await Promise.all([
    supabase.from('employees').select('*', { count: 'exact', head: true }),
    supabase.from('employees').select('*', { count: 'exact', head: true }).eq('status', 'active'),
    supabase.from('employees').select('*', { count: 'exact', head: true }).in('risk_level', ['critical', 'high']),
    supabase.from('access_reviews').select('*', { count: 'exact', head: true }).eq('decision', 'pending'),
    supabase.from('access_requests').select('*', { count: 'exact', head: true }).eq('status', 'pending'),
    supabase.from('employees').select('*', { count: 'exact', head: true }).eq('jml_stage', 'joiner'),
    supabase.from('employees').select('*', { count: 'exact', head: true }).eq('jml_stage', 'mover'),
    supabase.from('employees').select('*', { count: 'exact', head: true }).eq('jml_stage', 'leaver'),
    supabase.from('ai_insights').select('*', { count: 'exact', head: true }).eq('is_resolved', false),
  ])

  // Get risk distribution
  const { data: riskData } = await supabase
    .from('employees')
    .select('risk_level')

  const riskDist = { critical: 0, high: 0, medium: 0, low: 0 }
  riskData?.forEach((e: any) => {
    if (e.risk_level in riskDist) riskDist[e.risk_level as keyof typeof riskDist]++
  })

  return {
    totalUsers: totalUsers ?? 0,
    activeUsers: activeUsers ?? 0,
    highRiskUsers: highRiskUsers ?? 0,
    pendingReviews: pendingReviews ?? 0,
    pendingRequests: pendingRequests ?? 0,
    joiners: joiners ?? 0,
    movers: movers ?? 0,
    leavers: leavers ?? 0,
    unresolvedInsights: unresolvedInsights ?? 0,
    riskDistribution: riskDist,
  }
}

