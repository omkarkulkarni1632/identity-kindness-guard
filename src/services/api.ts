import { supabase } from '@/lib/supabase'

// =============================================
// EMPLOYEES
// =============================================

export async function getEmployees() {
  const { data, error } = await supabase
    .from('employees')
    .select(`
      *,
      departments(name),
      role_templates(name)
    `)
    .order('created_at', { ascending: false })

  if (error) throw error
  return data
}

export async function getEmployee(id: string) {
  const { data, error } = await supabase
    .from('employees')
    .select(`
      *,
      departments(name),
      role_templates(name, default_permissions),
      permissions(*, systems(name, is_critical))
    `)
    .eq('id', id)
    .single()

  if (error) throw error
  return data
}

export async function createEmployee(employee: {
  name: string
  email: string
  department_id: string
  role_template_id: string
  job_title: string
  join_date: string
}) {
  const { data, error } = await supabase
    .from('employees')
    .insert({
      ...employee,
      employee_id: '', // will be auto-generated by trigger
      jml_stage: 'joiner',
      status: 'pending',
    })
    .select()
    .single()

  if (error) throw error
  return data
}

export async function updateEmployee(id: string, updates: Record<string, any>) {
  const { data, error } = await supabase
    .from('employees')
    .update(updates)
    .eq('id', id)
    .select()
    .single()

  if (error) throw error
  return data
}

